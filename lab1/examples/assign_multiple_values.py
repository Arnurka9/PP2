#* Unpack a Collection
#In Python, you can extract values from a collection, such as a list or tuple, and assign them to variables. 
#This process is known as unpacking.


things = ["cup", "kettle", "fridge"]

something_1, something_2, something_3 = things
print(something_1)
print(something_2)
print(something_3)




